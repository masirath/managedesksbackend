const mongoose = require("mongoose");

const invoices_payments_schema = mongoose.Schema({
  invoice: {
    required: true,
    type: mongoose.Schema.Types.ObjectId,
    ref: "invoices",
  },
  name: {
    required: true,
    type: String,
  },
  amount: {
    required: true,
    type: Number,
    default: 0,
  },
  status: {
    required: true,
    type: Number,
    default: 1,
  },
  ref: {
    required: true,
    type: Number,
  },
  branch: {
    required: true,
    type: mongoose.Schema.Types.ObjectId,
    ref: "branches",
  },
  created: {
    required: true,
    type: Date,
  },
  created_by: {
    required: true,
    type: mongoose.Schema.Types.ObjectId,
    ref: "users",
  },
});

module.exports = mongoose.model(
  "invoices_payments",
  invoices_payments_schema,
  "invoices_payments"
);
